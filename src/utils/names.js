export const getName = (sexOpt) => {
  let sex;
  if (sexOpt === 'ランダム') {
    const i = getRandomNumber(0, 2);
    sex = i === 0 ? '男性' : '女性';
  } else {
    sex = sexOpt;
  }
  let lIndex = getRandomNumber(0, lastName.length);
  let lName = lastName[lIndex];
  let findex = 0;
  let fName = '';
  let result;
  switch (sex) {
    case '男性':
      findex = getRandomNumber(0, firstNameM.length);
      fName = firstNameM[findex];
      result = { name: `${lName} ${fName}`, sex };
      break;
    case '女性':
      findex = getRandomNumber(0, firstNameF.length);
      fName = firstNameF[findex];
      result = { name: `${lName} ${fName}`, sex };
      break;
    case '無し':
      const rFirstName = [...firstNameM, ...firstNameF];
      findex = getRandomNumber(0, rFirstName.length);
      fName = rFirstName[findex];
      result = {
        name: `${lName} ${fName}`,
        sex: null,
      };
  }
  return result;
};
